  "NFD of " + nfdStr;
  NFKC.normalize("NFD");
  NFKD;
  "NFD of " + nfkcStr;
  NFKD.normalize("NFD");
  NFKD;
  "NFD of " + nfkdStr;
  source.normalize("NFKC");
  NFKC;
  "NFKC of " + sourceStr;
  NFC.normalize("NFKC");
  NFKC;
  "NFKC of " + nfcStr;
  NFD.normalize("NFKC");
