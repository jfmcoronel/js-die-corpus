  "NFKD of " + sourceStr;
  NFC.normalize("NFKD");
  NFKD;
  "NFKD of " + nfcStr;
  NFD.normalize("NFKD");
  NFKD;
  "NFKD of " + nfdStr;
  NFKC.normalize("NFKD");
  NFKD;
